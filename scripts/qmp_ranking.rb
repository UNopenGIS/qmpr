      options[:format] = f
    end
    
    opts.on("-o", "--output FILE", String, "Output file path") do |o|
      options[:output] = o
    end
    
    opts.on("-h", "--help", "Show this help") do
      puts opts
      exit
    end
  end.parse!

  if ARGV.empty?
